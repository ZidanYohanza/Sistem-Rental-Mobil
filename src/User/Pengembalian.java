    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        int a,b,c;
        Peminjaman pem = new Peminjaman();
        a = Integer.valueOf(biayaPemPane.getText());
        b = Integer.valueOf(dendaPemPane.getText());
        c = a+b;
        pem.setPeminjaman(idPemPane2.getText());
        pem.setTelat(telatPane.getText());
        pem.setDenda(dendaPemPane.getText());
        pem.setBiaya(String.valueOf(c));
        pem.updatePeminjaman();
        javax.swing.JOptionPane.showMessageDialog(null, "Update data berhasil");
        idPemPane2.setText(null);
        idPemPane1.setText(null) ;
        jenisMobPane.setText(null);
        tglPemPane.setText(null); 
        lamaPemPane.setText(null); 
        biayaPemPane.setText(null); 
        tglKemPane.setText(null);
        telatPane.setText(null) ;
        dendaPemPane.setText(null) ;
    }//GEN-LAST:event_btnSaveActionPerformed

    

