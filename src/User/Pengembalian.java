    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        Koneksi kon = new Koneksi();
        ResultSet rs = null;
        try{ 
            rs = kon.getData("select * from peminjaman where id_peminjaman = '"+idPemPane1.getText()+"'");
            if(rs.next()){
                idPemPane2.setText(rs.getString("id_peminjaman"));
                jenisMobPane.setText(rs.getString("id_mobil"));
                tglPemPane.setText(rs.getString("tgl_pinjam"));
                lamaPemPane.setText(String.valueOf(rs.getInt("lama") ));
                biayaPemPane.setText(String.valueOf(rs.getInt("biaya" )));
                tglKemPane.setText(rs.getString("tgl_hrs_kembali"));
            } else {
                javax.swing.JOptionPane.showMessageDialog(null, "Data tidak ada di database");
            }
            rs.close();
        }catch(Exception e){
            javax.swing.JOptionPane.showMessageDialog(null, "Error:" + e.getMessage());
        }
    }//GEN-LAST:event_btnSearchActionPerformed

